    public boolean insertarAdministrador(int id, String nombre, String email, String contraseña) {
        try {
            CallableStatement stmt = con.prepareCall("{call ADMIN_DB.INSERTAR_ADMINISTRADORES(?, ?, ?, ?)}");
            stmt.setInt(1, id);
            stmt.setString(2, nombre);
            stmt.setString(3, email);
            stmt.setString(4, contraseña);
            ResultSet rs = stmt.executeQuery();

            stmt.close();
            return true;
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR AL INSERTAR ADMIN" + e);
            return false;

        }
    }
    
    
