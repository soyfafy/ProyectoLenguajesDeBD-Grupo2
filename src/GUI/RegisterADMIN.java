    private void btnRegistrarADMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarADMActionPerformed
        // Boton Ingresar
        try {
            int id = Integer.parseInt(txtIDAdmin.getText());
            String nombre = txtNomAdmin.getText();
            String email = txtCorreoAdmin.getText();
            String contraseña = String.valueOf(txtContraAdmin.getPassword());

            if (insertarAdministrador(id, nombre, email, contraseña)) {
                JOptionPane.showMessageDialog(rootPane, "DATOS GUARDADOS CON ÉXITO!");
                Inicio v1 = new Inicio();
                this.dispose();
                v1.show();
            } else {
                JOptionPane.showMessageDialog(null, "ERROR al guardar los datos. Inténtalo de nuevo.", "Error de guardado", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "ERROR! El ID debe ser un número entero.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "ERROR! " + ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarADMActionPerformed

